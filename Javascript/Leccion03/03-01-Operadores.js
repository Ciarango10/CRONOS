let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma: " + z);

z = a - b;
console.log("Resultado de la resta: " + z)

z = a * b;
console.log("Resultado de la multiplicacion: " + z)

z = a / b;
console.log("Resultado de la divisi√≥n: " + z)

z = a % b; //Residuo de la division
console.log("Resultado del residuo: " + z)

z = a ** b; //Potencia o exponente
console.log("Resultado de operador exponente: "+ z)

//Incremento
//Pre-incremento (el operador ++ antes de la variable)
z = ++a; 
console.log(a);
console.log(z);
//Post-incremento (el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//Decremento
//Pre-decremento
z = --a;
console.log(a);
console.log(z);
//Post-decremento
z = b--;
console.log(b);
console.log(z);
